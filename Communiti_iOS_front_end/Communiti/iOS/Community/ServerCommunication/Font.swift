import UIKit

class Font {
    
   class func LucidaSans(fontSize : CGFloat) -> UIFont? {
        return UIFont(name: "LucidaSans", size: fontSize)!
    }
    
   class func OpenSansRegular(fontSize : CGFloat) -> UIFont {
        return UIFont(name: "OpenSans-Regular", size: fontSize)!
    }
    
   class func OpenSansLight(fontSize : CGFloat) -> UIFont {
        return UIFont(name: "OpenSans-Light", size: fontSize)!
    }
    
   class func QuicksandRegular(fontSize : CGFloat) -> UIFont {
        return UIFont(name: "Quicksand-Regular", size: fontSize)!
    }
    
   class func ArimaMadurai_Light(fontSize : CGFloat) -> UIFont {
        return UIFont(name: "ArimaMadurai-Light", size: fontSize)!
    }
    
   class func ArimaMadurai_Medium(fontSize : CGFloat) -> UIFont {
        return UIFont(name: "ArimaMadurai-Medium", size: fontSize)!
    }
    
   class func ArimaMadurai_Regular(fontSize : CGFloat) -> UIFont {
        return UIFont(name: "ArimaMadurai-Regular", size: fontSize)!
    }
    
   class func ArimaMadurai_Thin(fontSize : CGFloat) -> UIFont {
        return UIFont(name: "ArimaMadurai-Thin", size: fontSize)!
    }
}
